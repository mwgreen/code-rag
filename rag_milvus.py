def _detect_embed_dim(model_path: str) -> int:
    """Read hidden_size from the model's config.json to determine embedding dimension."""
    config_path = Path(model_path) / "config.json"
    if not config_path.exists():
        raise RuntimeError(f"Model config not found at {config_path}")
    with open(config_path) as f:
        config = json.load(f)
    dim = config.get("hidden_size")
    if not dim:
        raise RuntimeError(f"hidden_size not found in {config_path}")
    return dim


def _get_embed_dim() -> int:
    """Get embedding dimension, detecting from model config if needed."""
    global _EMBED_DIM
    if _EMBED_DIM is None:
        _EMBED_DIM = _detect_embed_dim(_MODEL_PATH)
    return _EMBED_DIM


def compute_file_hash(file_path: str) -> str:
    """Compute SHA256 hash of file content for change detection."""
    try:
        with open(file_path, 'rb') as f:
            return hashlib.sha256(f.read()).hexdigest()
    except Exception:
        return ""


def is_jaxb_generated(file_path: str) -> bool:
    """Check if a Java file is JAXB-generated by looking for the marker."""
    try:
        with open(file_path, 'r', encoding='utf-8', errors='ignore') as f:
            lines = [f.readline() for _ in range(50)]
            content = ''.join(lines)
            if 'schema fragment specifies the expected content' in content:
                return True
            if 'generated by the JavaTM Architecture for XML Binding' in content:
                return True
            if '@XmlRootElement' in content and '@XmlType' in content:
                return True
        return False
    except Exception:
        return False


